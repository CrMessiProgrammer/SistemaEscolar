    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/escolar","root","");
            Statement st = (Statement)conn.createStatement();
            String sql = "select * from cadprofessor";

            PreparedStatement ptst = (PreparedStatement)conn.prepareStatement(sql);
            java.sql.ResultSet rs = ptst.executeQuery();
            DefaultTableModel tm = (DefaultTableModel)proftabela.getModel();
            tm.setRowCount(0);
            while(rs.next()){
                Object o[] = {rs.getInt("id"),rs.getString("nome"),rs.getInt("rg"),
                rs.getInt("cpf"),rs.getString("email"),rs.getString("disciplina")};
                tm.addRow(o);
            }
            }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

