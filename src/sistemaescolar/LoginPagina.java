    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = user.getText();
        String p = pass.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/escolar","root","");
            Statement st = (Statement)conn.createStatement();
            String sql = "select * from usuario_login";
            
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                String username = rs.getString("usuario");
                String password = rs.getString("senha");
                
            if(un.equals(username) && p.equals(password)){
                new obrigado().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "usuario ou senha incorreta!");
                user.setText("");
                pass.setText("");
            }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro ao conectar  ao banco de dados");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

